#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ss;

	i = 0;
	ss = (unsigned char *)s;
	while (n--)
	{
		ss[i] = (unsigned char)c;
		i++;
	}
	return ((void *)s);
}
